<script setup>
import { onMounted, ref } from 'vue';

const model = defineModel({
    type: String,
    required: true,
});

const input = ref(null);

onMounted(() => {
    if (input.value.hasAttribute('autofocus')) {
        input.value.focus();
    }
});

defineExpose({ focus: () => input.value.focus() });
</script>

<template>
    <input
        class="border border-brand-primary focus:border-brand-secondary !focus:ring-brand-secondary rounded-lg shadow-sm p-3"
        v-model="model"
        ref="input"
    />
</template>
